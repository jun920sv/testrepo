package practice02_print01;

public class Main {

	public static void main(String[] args) {
		// int 型（整数型）の変数 a に 10 を代入
		int a =10;
		// boolean 型（真偽値型）の変数 b に 真の値 を代入してください
		boolean b = true;
		// char 型（文字型）の変数 c に A を代入してください
		char c = 'A';
		// double 型（小数型）の変数 d に 3.14 を代入してください
		double d = 3.14;
		// float 型（小数型）の変数 f に 1.21 を代入
		float f = 1.21F;
		// String 型（文字列型）の変数 s に Hello World を代入してください
		String s ="Hello World";
		// 変数a , b , c , d , f, s をそれぞれ表示してください。
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(f);
		System.out.println(s);
		
	}

}
