package print10;

public class Person3 {

	private String name;
	private String tel;
	

	public Person3(String name,String tel) {
		this.name=name;
		
		this.tel=tel;	
	}
	
	public String getName() {
		return name;
	}
	
	public String getTel() {
		return tel;
	}
	
	public void setName(String name) {
		this.name =name;
	}
	public void setTel(String tel) {
		this.tel =tel;
	}
	
	
	
}
